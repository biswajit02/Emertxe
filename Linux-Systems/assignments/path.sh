source prompt.sh


#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------START OF THE PROGRAM-------------------------------------------------------------------

#capturing the path 
path=(`echo $PATH | tr ":" "\n"`)
echo ${path[@]}
#intialisg the count
tcount=0

#using the loop
for (( i=0; i<${#path[@]}; i++ ))
do
            tcount=$(($count+$tcount))
            echo "DIRECTORY: ${path[i]}"
            count=`find ${path[i]} -executable | wc -l`
            echo "$count"
done
 
#printing the final count
    echo "Toatal count: $tcount"


